// Дан одномерный массив, длину массива задаёт пользователь. Вычислить сумму квадратов тех чисел, модуль которых превышает значение 2,5.

#include <iostream>
#include <cmath> // Для функции fabs

int main() {
    int length;

    // Запрашиваем у пользователя длину массива
    std::cout << "Введите длину массива: ";
    std::cin >> length;

    // Проверка на корректность ввода
    if (length <= 0) {
        std::cout << "Длина массива должна быть положительным числом!" << std::endl;
        return 1; // Завершаем программу с ошибкой
    }

    // Создаем массив заданной длины
    double* array = new double[length];

    // Запрашиваем у пользователя ввод элементов массива
    std::cout << "Введите " << length << " элементов массива: ";
    for (int i = 0; i < length; ++i) {
        std::cin >> array[i];
    }

    // Переменная для хранения суммы квадратов
    double sum_of_squares = 0.0;

    // Вычисляем сумму квадратов чисел с модулем больше 2.5
    for (int i = 0; i < length; ++i) {
        if (fabs(array[i]) > 2.5) { // Проверяем модуль числа
            sum_of_squares += array[i] * array[i]; // Добавляем квадрат к сумме
        }
    }

    // Выводим результат
    std::cout << "Сумма = " << sum_of_squares << std::endl;

    // Освобождаем выделенную память
    delete[] array;

    return 0;
}
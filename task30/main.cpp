/*
Используя оператор цикла do while, составить программу, которая будет требовать ввод цифр с клавиатуры до тех пор, пока не будет введена цифра 1.
*/

#include <iostream>
using namespace std;

int main() {
    char input; // Переменная для хранения введенного символа

    do {
        // Запрашиваем ввод у пользователя
        cout << "Введите цифру (введите '1' для выхода): ";
        cin >> input; // Считываем один символ

        // Проверяем, является ли введенное значение цифрой
        if (isdigit(input)) {
            cout << "Вы ввели: " << input << endl; // Выводим введенное значение
        } else {
            cout << "Ошибка: введено не число." << endl; // Сообщаем об ошибке
        }

    } while (input != '1'); // Продолжаем цикл, пока не введена цифра '1'

    cout << "Вы вышли из программы." << endl; // Сообщаем о выходе из программы
    return 0; // Завершение программы
}

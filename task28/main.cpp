/*
Программа должна находить точку пересечения двух линий. Даны следующие уравнения: y = ax2 + bx + c и y = dx + k. Как вы уже могли понять, это уравнения линий, где a, b, c, d, k — это числа, которые вводятся с клавиатуры. Необходимо найти значение икса.

Итак необходимо организовать ввод чисел с клавиатуры. После этого, второе уравнение подставить вместо игрика в первое, получися равенство, которое не сложно решается. В итоге, программа должна вернуть два числа: x и y. 
*/

#include <iostream>
#include <cmath> // Для использования функции sqrt
using namespace std;

int main() {
    // Объявление переменных для коэффициентов
    double a, b, c, d, k;

    // Ввод коэффициентов для первого уравнения
    cout << "y = ax^2 + bx + c - первое уравнение. Введите коэффициенты a, b, c" << endl;
    cout << "a = ";
    cin >> a;
    cout << "b = ";
    cin >> b;
    cout << "c = ";
    cin >> c;

    // Ввод коэффициентов для второго уравнения
    cout << "y = dx + k - второе уравнение. Введите коэффициенты d, k" << endl;
    cout << "d = ";
    cin >> d;
    cout << "k = ";
    cin >> k;

    // Подстановка второго уравнения в первое
    // Получаем уравнение ax^2 + (b - d)x + (c - k) = 0
    double A = a;
    double B = b - d;
    double C = c - k;

    // Вычисление дискриминанта
    double discriminant = B * B - 4 * A * C;

    // Проверка дискриминанта
    if (discriminant < 0) {
        cout << "Нет действительных решений." << endl;
    } else {
        // Вычисление корней уравнения
        double x1 = (-B + sqrt(discriminant)) / (2 * A);
        double x2 = (-B - sqrt(discriminant)) / (2 * A);

        // Вычисление соответствующих значений y
        double y1 = d * x1 + k;
        double y2 = d * x2 + k;

        // Вывод результатов
        cout << "Точки пересечения двух данных линий:" << endl;
        cout << "1: [" << x1 << ", " << y1 << "]" << endl;
        cout << "2: [" << x2 << ", " << y2 << "]" << endl;
    }

    return 0; // Завершение программы
}

